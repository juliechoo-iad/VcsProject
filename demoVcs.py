class Calculator:
    def add(num1, num2):
        print("Sum is ",(num1+num2))
    def minus(num1, num2):
        print("Difference is ",(num1 - num2))
    def multiply(num1, num2):
        print("Product is ",(num1 * num2))


Calculator.add(50,40)
Calculator.minus(50,40)
Calculator.multiply(50,40)